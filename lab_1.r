# Есть набор данных:
df <- data.frame(var1=c(11,21,31), var2=c(12,22,32), var3=c(13,23,33), var4=c(14,24,34), row.names=c("case1", "case2", "case3"))

# выведем его
df

# Выбрать значения var1, var2, var3 для case1
df["case1", 1:3]

# Выбрать значения всех переменных для case2, которые больше 22
df["case2", 1:4][which(df["case2", 1:4] > 22)]

# Выбрать имена переменных для колонок 1 и 3
c(names(df)[1], names(df)[3])

# Добавить колонку с именем Y и значениями -1, 0, 1
Y <- c(-1, 0, 1)
Y
df <- cbind(df, Y)
df

# Удалить строку case2
df <- df[-2,]
df

# Значения второй колонки возвести в третью степень
df["var2"] <- df["var2"] * df["var2"] * df["var2"]
